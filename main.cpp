/*
1. Declaration (Объявление)
Что это: Объявление сообщает компилятору: "Эй, такое имя существует, и оно имеет вот такой тип".
Объявление не выделяет память и не содержит реализации (тела функции или кода). Оно нужно только для того, чтобы компилятор знал, что этот элемент где-то есть, и не ругался, когда вы его используете.
*/

/*
ПРИМЕР
int a;          // Объявление переменной (в глобальной области это также и определение, см. ниже)
extern int b;   // Чистое объявление переменной (говорит: "b где-то есть, ищи в другом файле")
void f(int x);  // Объявление функции (прототип). Мы сказали, что она есть, но не написали, что она делает.
class C;        // Объявление класса (опережающее объявление)
struct S;       // Объявление структуры
*/

/*
Definition (Определение)
Что это: Определение сообщает компилятору: "А вот и сам этот элемент, выделите для него память" (для переменных) или "А вот и код, который эта функция выполняет" (для функций).
Определение всегда является и объявлением тоже, но добавляет к нему суть (тело или память).
Аналогия: Это сама глава в книге с полным текстом.
*/
