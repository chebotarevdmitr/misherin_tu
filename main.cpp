/*
Scope (Область видимости)
Что это: Область видимости определяет, в какой части кода вы можете использовать это имя (переменную, функцию, класс). Как только программа выходит за пределы этой области, имя перестаёт быть доступным (а для локальных переменных в этот момент память освобождается).
Аналогия: Представьте, что вы говорите. Если вы кричите на стадионе (глобальная область), вас слышат все. Если вы шепчетесь в закрытой комнате (локальная область), вас слышат только те, кто внутри этой комнаты.
*/